# DEFINICIÓN DEL INTÉRPRETE
print("\n> Definición del Intérprete de Python")
print("- Es un programa que ejecuta el código fuente de Python línea por línea.")
print("- Actúa como un traductor en tiempo real: lee cada línea y la convierte en instrucciones que la computadora entiende.")
print("- Si hay un error en el código, muestra el mensaje de error y el lugar exacto del problema.")
print("- Permite el uso del 'modo interactivo', donde podemos escribir comandos y ver los resultados al instante.")

# COMPARACIÓN CON COMPILADOR
print("\n> Diferencia con un Compilador")
print("- El compilador traduce todo el código fuente de una vez y genera un archivo ejecutable.")
print("- El archivo resultante se puede ejecutar varias veces sin necesidad de volver a compilar.")
print("- Los programas compilados suelen ser más rápidos porque no se interpretan línea por línea cada vez.")
print("- Ejemplos de lenguajes compilados: C, C++, Rust.")

print("Python utiliza un intérprete para ejecutar el código, no un compilador.")
print("Esto lo hace muy flexible y fácil para depurar errores, aunque puede ser ligeramente más lento que los lenguajes compilados.")

