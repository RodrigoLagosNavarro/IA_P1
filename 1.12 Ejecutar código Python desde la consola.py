# EJECUTAR CÓDIGO EN LA CONSOLA
print("\n> Ejecutar código Python en la consola")
print("- Abre CMD o cualquier consola de Windows.")
print("- Escribe el comando: python")
print("- Si el PATH de Python está bien configurado, se abrirá el intérprete y verás el prompt >>>")
print("- Ahora puedes escribir instrucciones línea por línea. Ejemplo:")
print('  >>> print("Hola desde la consola!")')
print("- El resultado aparecerá inmediatamente.")
print("- Puedes usar el intérprete como calculadora: >>> 3 + 5")
print("- Para salir, usa exit(), quit() o cierra la ventana de la consola.")

# EJECUTAR CÓDIGO DESDE ARCHIVO DE TEXTO
print("\n> Ejecutar código Python desde un archivo de texto")
print("- Abre un Bloc de notas en Windows o cualquier editor de texto plano.")
print('- Escribe código Python, por ejemplo: print("Probando Python en archivo .py")')
print("- Guarda el archivo con extensión .py (por ejemplo: prueba.py)")
print("- IMPORTANTE: Desactiva 'Ocultar extensiones de archivo conocidas' en el Explorador de Windows")
print("- Abre CMD, navega a la carpeta donde está el archivo o usa la ruta completa.")
print("- Ejecuta el archivo con: python ruta/archivo.py")
print("- El intérprete ejecutará el archivo y mostrará el resultado en consola.")

# EJECUTAR EN LINUX
print("\n> Ejecutar código Python en Linux")
print("- Abre la Terminal y escribe: python3")
print("- Usa el mismo procedimiento para ejecutar línea por línea.")
print("- Para crear un archivo .py desde la terminal, usa: nano nombre.py")
print("- Escribe el código, guarda con CTRL + O y sal con CTRL + X.")
print("- Ejecuta con: python3 nombre.py (si estás en la misma carpeta).")

# CONCEPTOS CLAVE
print("\n> Conceptos clave")
print("- Ruta absoluta: Contiene el camino completo desde la raíz del disco hasta el archivo.")
print("- Ruta relativa: Solo indica el camino desde la carpeta actual hasta el archivo.")
print("- Ambas sirven para ejecutar archivos, dependiendo de dónde te encuentres en la consola.")

